#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned long	i;
	unsigned char	*ptr;

	i = 0;
	ptr = s;
	while (i < n)
	{
		if (n == 0)
			break ;
		else
		{
			ptr[i] = '\0';
			i++;
		}
	}
}
