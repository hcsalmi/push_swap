#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*arr;

	i = 0;
	if (s == NULL)
		return (NULL);
	if (ft_strlen(s) == 0)
		return (NULL);
	arr = ft_strdup(s);
	if (arr == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		arr[i] = f(i, s[i]);
		i++;
	}
	return (arr);
}
