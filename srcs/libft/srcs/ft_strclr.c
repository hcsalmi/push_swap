#include "libft.h"

void	ft_strclr(char *s)
{
	int	i;
	int	length;

	i = 0;
	if (s == NULL)
		return ;
	length = ft_strlen(s);
	while (i < length)
	{
		s[i] = '\0';
		i++;
	}
}
